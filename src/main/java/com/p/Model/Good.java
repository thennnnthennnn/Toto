package com.p.Model;

public class Good {
    private int id;
    private String name;
    private int inventory;
    private int price;
    public int getId(){
        return id;
    }

    public int getInventory() {
        return inventory;
    }

    public int getPrice() {
        return price;
    }

    public String getName() {
        return name;
    }
}
